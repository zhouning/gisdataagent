"""Emit a representativeness table for the GIS Spatial benchmark.

Counts across all non-Robustness questions:
  - OGC SFA predicate family (topological / metric / KNN / aggregation / plain)
  - PostGIS function frequency (ST_Intersects, ST_DWithin, ...)
  - Difficulty split

The gold SQL is the source of truth for predicate/function classification.
Output: LaTeX tabular for \\input{} in paper §4.1.

Responds to 2026-05-07 reviewer A §3.1 ("benchmark construction table")
and reviewer B §2 (85q representativeness).
"""
from __future__ import annotations

import json
import re
from collections import Counter
from pathlib import Path

BENCH = Path("D:/adk/benchmarks/chongqing_geo_nl2sql_100_benchmark.json")
OUT_TEX = Path("D:/adk/submission/nl2semantic2sql_v4/table_benchmark_profile.tex")

TOPO_FNS = ["ST_Intersects", "ST_Contains", "ST_Within",
            "ST_Touches", "ST_Crosses", "ST_Overlaps", "ST_Equals"]
METRIC_FNS = ["ST_Distance", "ST_Length", "ST_Area", "ST_DWithin"]
OTHER_PG_FNS = ["ST_Buffer", "ST_Transform", "ST_Centroid", "ST_Union",
                "ST_MakePoint", "ST_AsText", "ST_AsGeoJSON"]
KNN_OP = "<->"


def classify_families(sql: str) -> list[str]:
    """A single query can belong to multiple families (metric + aggregation)."""
    up = sql.upper()
    fams: list[str] = []
    if any(fn.upper() in up for fn in TOPO_FNS):
        fams.append("topological")
    if any(fn.upper() in up for fn in METRIC_FNS):
        fams.append("metric")
    if KNN_OP in sql:
        fams.append("knn")
    if (re.search(r"\bGROUP\s+BY\b", up)
            or re.search(r"\b(SUM|COUNT|AVG|MIN|MAX)\s*\(", up)):
        fams.append("aggregation")
    return fams or ["plain"]


def main() -> None:
    qs = json.loads(BENCH.read_text(encoding="utf-8"))
    spatial = [q for q in qs if q.get("difficulty") != "Robustness"]
    n = len(spatial)

    fam = Counter()
    fns = Counter()
    diff = Counter()
    joins = Counter()  # rough: count FROM/JOIN tables per query

    for q in spatial:
        gold = q.get("golden_sql", "") or ""
        for f in classify_families(gold):
            fam[f] += 1
        for fn_name in TOPO_FNS + METRIC_FNS + OTHER_PG_FNS:
            if fn_name.upper() in gold.upper():
                fns[fn_name] += 1
        if KNN_OP in gold:
            fns[KNN_OP] += 1
        diff[q.get("difficulty", "?")] += 1
        # Rough join multiplicity: count distinct cq_* table references
        tables = set(re.findall(r"\bcq_[a-z0-9_]+", gold))
        key = "single" if len(tables) <= 1 else ("two" if len(tables) == 2 else "three_or_more")
        joins[key] += 1

    lines = [
        r"% Auto-generated by scripts/nl2sql_bench_cq/benchmark_profile.py",
        r"% Regenerate after any benchmark change; do not hand-edit.",
        r"\begin{tabular}{lr}",
        r"\toprule",
        r"Category & Count \\",
        r"\midrule",
        r"\multicolumn{2}{l}{\emph{Predicate family (a query may belong to several)}} \\",
    ]
    for label in ["topological", "metric", "knn", "aggregation", "plain"]:
        lines.append(f"\\quad {label} & {fam[label]} \\\\")
    lines.append(r"\multicolumn{2}{l}{\emph{PostGIS function frequency}} \\")
    for fn_name, c in fns.most_common():
        if fn_name == KNN_OP:
            safe = r"\texttt{<->}"
        else:
            escaped = fn_name.replace("_", r"\_")
            safe = r"\texttt{" + escaped + "}"
        lines.append(f"\\quad {safe} & {c} \\\\")
    lines.append(r"\multicolumn{2}{l}{\emph{Multi-table join multiplicity}} \\")
    for label, key_name in [("single-table", "single"), ("2-table join", "two"), ("3+ table join", "three_or_more")]:
        lines.append(f"\\quad {label} & {joins[key_name]} \\\\")
    lines.append(r"\multicolumn{2}{l}{\emph{Difficulty stratification}} \\")
    for d in ["Easy", "Medium", "Hard"]:
        lines.append(f"\\quad {d} & {diff[d]} \\\\")
    lines.append(r"\midrule")
    lines.append(f"Total Spatial questions & {n} \\\\")
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")

    OUT_TEX.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {OUT_TEX}")
    print("\n".join(lines))


if __name__ == "__main__":
    main()
